// operador de comparacion
3 == "3"; // true
3 === "3"; // false. Porque tienen que ser exactamente igual como el tipo de valor.
5 < 3; // false, no es falso
5 > 3; // true
5 <= 6; // true. 5 es menor o igual que 6
5 >= 6 // false


// estamos en una condicion de que las dos sean verdad
a && b; // true. Si alguna de las dos es falso no se cumplira.

// validacion. Si ambos son iguales o si alguno de los dos es true
a || b; // alguna de estas dos tiene que ser verdad, se cumple algo.


