// Validacion de casos
// es una forma de condicion o validacion. pero funciona por casos. si el caso es verdad pasa esa parte si no es verda pasa otro caso deseado. Si ninguno de los casos es verdad entra la etapa de default
// sintaxis

var numero = 1;

switch(numero) {
    case 1:
        console.log("Soy un 1");
        break;
    case 10:
        console.log("soy un 10");
        break;
    case 30:
        console.log("soy un 30");
        break;
    default:
        console.log("no soy nada");
}
numero;